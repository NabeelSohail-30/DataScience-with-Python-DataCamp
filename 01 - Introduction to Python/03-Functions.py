# type function
days = 7
print(type(days))

# max function
fam = [1.73, 1.68, 1.71, 1.89]
print(max(fam))

# round function
print(round(1.68, 1))

# sorted function
marks = [1, 4, 5, 6, 9, 3, 0]
sorted_marks = sorted(marks)
print(sorted_marks)

# sum function
print(sum(marks))

# help function
help(round)

# List Methods
# append method
fam.append(1.79)
print(fam)

# index method
print(fam.index(1.68))

# count method
print(fam.count(1.68))

# reverse method
fam.reverse()
print(fam)

# sort method
fam.sort()
print(fam)

# pop method
fam.pop()
print(fam)

# remove method
fam.remove(1.68)
print(fam)

# String Methods
# replace method
sister = "liz"
sister = sister.replace("z", "sa")
print(sister)

# upper method
sister = sister.upper()
print(sister)

# capitalize method
sister = sister.capitalize()
print(sister)

# count method
print(sister.count("s"))
